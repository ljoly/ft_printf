#include "libft.h"

void	ft_putnbr_st(size_t n)
{
	if (n <= 9)
		ft_putchar(n + '0');
	if (n >= 10)
	{
		ft_putnbr_st(n / 10);
		ft_putchar(n % 10 + '0');
	}
}
